import javax.swing.*;
import java.awt.*;
//--------------------------------------------------------------------------------
//Block klass
//--------------------------------------------------------------------------------
public class Block extends JPanel{
    //--------------------------------------------------------------------------------
//int Deklarationer 
//--------------------------------------------------------------------------------
    public int posX;
    public int posY;
    public int width;
    public int height;

    boolean visible = true;
    //--------------------------------------------------------------------------------
//Konstruktor
//--------------------------------------------------------------------------------
    public Block(int x, int y, int w, int h){
        posX = x;
        posY = y;
        width = w;
        height = h;
        repaint();
    }//end Block
    //--------------------------------------------------------------------------------
// Break - inte klar
//--------------------------------------------------------------------------------	
    public void Break(){
// 		System.out.println("break");
        visible = false;
    }//end
    //--------------------------------------------------------------------------------
//Draw Block
//--------------------------------------------------------------------------------	
    public void paint(Graphics g){
        if(visible){
            g.setColor(new Color(121, 252, 61));
            g.fillRect(posX,posY,width,height);
        }//end if visible
    }//end paint
}//end class
